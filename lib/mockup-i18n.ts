const TABLES: Record<Locale, StringTable> = {
  en: EN,
  vi: VI,
  "zh-HK": EN, // fallback to English for zh-HK in mockups for now
};

/**
 * Translate a dotted key for the mockups.
 * Falls back to English when a key is missing in the target locale,
 * and finally to the key itself when nothing matches.
 */
export function mt(key: string, lang: Locale | undefined): string {
  const [ns, k] = key.split(".");
  if (!ns || !k) return key;
  const table = TABLES[lang ?? "en"] ?? EN;
  return table[ns]?.[k] ?? EN[ns]?.[k] ?? key;
}
